    public void Seguimiento()
    {
        int id = Integer.parseInt(this.jTextField1.getText());
        this.jTable1.setModel(modelo);
        int filas = this.jTable1.getRowCount();
        for (int i = 0; i < filas; i++)
        {
            modelo.removeRow(0);
        }
        
        this.jTable2.setModel(modelo2);
        int filas2 = this.jTable2.getRowCount();
        for (int i = 0; i < filas2; i++) {
            modelo2.removeRow(0);
        }
        
        String[] datos2 = new String[3];
        
        String[] datos = new String[3];
        
        for (int i = 1; i <= expedientes.longitud(); i++) {
            if (id == expedientes.iesimo(i).getIdentificador()){
                datos[0] = "Expediente";
                datos[1] = expedientes.iesimo(i).getFecharegistro().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss"));
                if (expedientes.iesimo(i).getFechafinalizacion() == null){
                    datos[2] = "---";
                }else{
                    datos[2] = expedientes.iesimo(i).getFechafinalizacion().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss"));
                }
                modelo.addRow(datos);
                
                for (int j = 1; j <= expedientes.iesimo(i).getMovimientos().longitud(); j++) {
                    datos[0] = expedientes.iesimo(i).getMovimientos().iesimo(j).getMovimiento();
                    datos[1] = expedientes.iesimo(i).getMovimientos().iesimo(j).getFechainicio().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss"));
                    if (expedientes.iesimo(i).getMovimientos().iesimo(j).getFechafinal() == null){
                        datos[2] = "---";
                    }else{
                        datos[2] = expedientes.iesimo(i).getMovimientos().iesimo(j).getFechafinal().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss"));
                    }
                    modelo.addRow(datos);
                }
                
                for (int j = 1; j <= expedientes.iesimo(i).getDocumentos().longitud(); j++) {
                    datos2[0] = expedientes.iesimo(i).getDocumentos().iesimo(j).getDepartamento();
                    datos2[1] = expedientes.iesimo(i).getDocumentos().iesimo(j).getTitulo();
                    datos2[2] = expedientes.iesimo(i).getDocumentos().iesimo(j).getRemitente();
                    modelo2.addRow(datos2);
                }
            }
        }
        this.jTable1.setDefaultEditor(Object.class, null);
        this.jTable2.setDefaultEditor(Object.class, null);
    }
    

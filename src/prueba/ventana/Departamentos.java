    public void Llenar_Dependencia()
    {
        this.jTable1.setModel(modelo);
        int filas = this.jTable1.getRowCount();
        for (int i = 0; i < filas; i++)
        {
            modelo.removeRow(0);
        }
        Cola<Expediente> colaAux = new Cola<>();
        Cola<Expediente> colaAlta = new Cola<>();
        Cola<Expediente> colaMedia = new Cola<>();
        Cola<Expediente> colaBaja = new Cola<>();
        String[] datos = new String[9];
        String opcion = (String)this.jComboBox1.getSelectedItem();
        if (opcion!= null){
            for (int i = 1; i <= this.dependencias.longitud(); i++) {
            if (opcion.equals(dependencias.iesimo(i).getNombre())){
                this.jTextField4.setText("Departamento de " + dependencias.iesimo(i).getNombre());
                this.jTextField5.setText("Jefe: " + dependencias.iesimo(i).getJefe());
                
                while(!dependencias.iesimo(i).getExpedientes().esVacia()){
                    Expediente exp = dependencias.iesimo(i).getExpedientes().desencolar();
                    String prioridad = exp.getPrioridad();
                    
                    if (prioridad.equalsIgnoreCase("Alta")){
                        colaAlta.encolar(exp);
                    }else if (prioridad.equalsIgnoreCase("Media")){
                        colaMedia.encolar(exp);
                    }else if (prioridad.equalsIgnoreCase("Baja")){
                        colaBaja.encolar(exp);
                    }
                }
                
                while (!colaAlta.esVacia()){
                    dependencias.iesimo(i).getExpedientes().encolar(colaAlta.desencolar());
                }
                while (!colaMedia.esVacia()){
                    dependencias.iesimo(i).getExpedientes().encolar(colaMedia.desencolar());
                }
                while (!colaBaja.esVacia()){
                    dependencias.iesimo(i).getExpedientes().encolar(colaBaja.desencolar());
                }
                
                while(!dependencias.iesimo(i).getExpedientes().esVacia()){
                    Expediente exp = dependencias.iesimo(i).getExpedientes().desencolar();
                    datos[0] = String.valueOf(exp.getIdentificador());
                    datos[1] = exp.getPrioridad();
                    datos[2] = String.valueOf(exp.getDatosint().getDni());
                    datos[3] = exp.getDatosint().getNombres();
                    datos[4] = String.valueOf(exp.getDatosint().getTelefono());
                    datos[5] = exp.getDatosint().getEmail();
                    datos[6] = exp.getAsunto();
                    datos[7] = exp.getFecharegistro().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss"));
                    datos[8] = exp.getDatosint().getTipo();
                    modelo.addRow(datos);
                    colaAux.encolar(exp);
                }
                
                while (!colaAux.esVacia()){
                    dependencias.iesimo(i).getExpedientes().encolar(colaAux.desencolar());
                }
            }
        }
        }
        
        this.jTable1.setDefaultEditor(Object.class, null);
    }
    

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        boolean encontrado = false;
        boolean mismo = false;
        int idbuscado = Integer.parseInt(this.jTextField1.getText());
        String inicio = (String)this.jComboBox1.getSelectedItem();
        String destino = (String)this.jComboBox2.getSelectedItem();
        if (inicio != destino){
            for (int i = 1; i <= dependencias.longitud(); i++) {
                if (dependencias.iesimo(i).getNombre().equalsIgnoreCase(inicio)){
                    Cola<Expediente> aux = new Cola<>();
                    while(!dependencias.iesimo(i).getExpedientes().esVacia()){
                        Expediente expaux = dependencias.iesimo(i).getExpedientes().desencolar();
                        if (expaux.getIdentificador() == idbuscado){
                            for (int j = 1; j <= dependencias.longitud(); j++) {
                                if (dependencias.iesimo(j).getNombre().equalsIgnoreCase(destino)){
                                    LocalDateTime fecha = LocalDateTime.now();
                                    Movimientos mov = new Movimientos(destino, fecha, null);
                                    expaux.getMovimientos().iesimo(expaux.getMovimientos().longitud()).setFechafinal(fecha);
                                    expaux.getMovimientos().agregar(mov);
                                    dependencias.iesimo(j).getExpedientes().encolar(expaux);
                                    encontrado = true;

                                }
                            }
                        }else{
                            aux.encolar(expaux);
                        }
                    }

                    while (!aux.esVacia()){
                        dependencias.iesimo(i).getExpedientes().encolar(aux.desencolar());
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "ERROR: lo esta moviendo al mismo departamento");
            mismo = true;
        }
        if (!encontrado && !mismo){
            JOptionPane.showMessageDialog(null, "ERROR: no se encontro el id: " + idbuscado + "\nEn la cola: " + inicio);
        }

        Llenar_Dependencia();
    }//GEN-LAST:event_jButton4ActionPerformed

